using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Z59
{
    class Racun
    {
        int suma = 0;
        private int a, b;

        public Racun(int m, int n)
        {
            this.a = m;
            this.b = n;
        }

        public int Izracunaj()
        {
            return suma = a + b;
        }

    }
}
